// tools/backup-db.js — גיבוי.
//
// ============================================================
// הגיבוי המקומי הושבת במעבר ל-Supabase — וזו החלטה מכוונת.
// ============================================================
// הכלי הזה השתמש ב-backup API של better-sqlite3 כדי להעתיק את קובץ
// sitemonitor.db ל-Google Drive. אחרי ההגירה הקובץ הזה כבר לא מכיל את
// הנתונים — הם ב-Postgres בענן — ולכן גיבוי שלו היה מייצר *אשליה של גיבוי*:
// קובץ שנוצר כל יום, נראה תקין, ולא שווה כלום ביום שבו יזדקקו לו.
// זו תקלה גרועה יותר מאי-גיבוי, כי היא שקטה.
//
// Supabase מגבה את בסיס הנתונים אוטומטית (Daily backups בתוכנית החינמית,
// Point-in-Time Recovery בתוכניות בתשלום). זה הגיבוי הפעיל היום.
//
// אם בהמשך יידרש גיבוי *מקומי* נוסף (למשל עותק מחוץ ל-Supabase), הדרך
// הנכונה היא pg_dump — לא העתקת קובץ. משאירים כאן פונקציה שלא עושה כלום
// כדי שלולאת התחזוקה היומית ב-master.js תמשיך לעבוד בלי שינוי.
// ============================================================

async function runBackup() {
  console.log("[backup] דולג — Supabase מגבה את בסיס הנתונים אוטומטית.");
}

if (require.main === module) {
  runBackup();
}

module.exports = { runBackup };
